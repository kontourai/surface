import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const scriptSourcePath = path.join(root, "src", "console", "client", "index.js");
const scriptPartDir = path.join(root, "src", "console", "client", "parts");
const styleSourcePath = path.join(root, "src", "console", "styles", "index.css");
const generatedPath = path.join(root, "src", "console", "assets.generated.ts");
const checkOnly = process.argv.includes("--check");

const scriptPartFiles = [
  "state.js",
  "format.js",
  "analysis.js",
  "dashboard.js",
  "detail.js",
  "routing-help.js",
  "authoring.js",
  "runs.js",
];

const [scriptPartSources, styleSource] = await Promise.all([
  Promise.all(scriptPartFiles.map((file) => readFile(path.join(scriptPartDir, file), "utf8"))),
  readFile(styleSourcePath, "utf8"),
]);

const scriptSource = `${scriptPartSources.map((source) => source.trimEnd()).join("\n\n")}\n`;
const scriptSourceFile = [
  "// Generated by scripts/build-console-assets.mjs from src/console/client/parts/.",
  "// Do not edit directly; edit the ordered part files instead.",
  `// Build order: ${scriptPartFiles.join(", ")}`,
  "",
].join("\n");

const generated = [
  "// Generated by scripts/build-console-assets.mjs. Do not edit directly.",
  "/* eslint-disable */",
  `export const CONSOLE_SCRIPT: string = ${JSON.stringify(scriptSource)};`,
  `export const CONSOLE_CSS: string = ${JSON.stringify(styleSource)};`,
  "",
].join("\n");

if (checkOnly) {
  const [currentScript, currentGenerated] = await Promise.all([
    readFile(scriptSourcePath, "utf8").catch(() => ""),
    readFile(generatedPath, "utf8").catch(() => ""),
  ]);
  if (currentScript !== scriptSourceFile || currentGenerated !== generated) {
    throw new Error("Console assets are out of sync. Run npm run build:console-assets.");
  }
  console.log("Surface Console generated assets are synced.");
} else {
  await Promise.all([
    mkdir(path.dirname(scriptSourcePath), { recursive: true }),
    mkdir(path.dirname(generatedPath), { recursive: true }),
  ]);
  await Promise.all([
    writeFile(scriptSourcePath, scriptSourceFile),
    writeFile(generatedPath, generated),
  ]);
  console.log("Built Surface Console generated assets.");
}
