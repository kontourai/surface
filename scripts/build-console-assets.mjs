import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const scriptSourcePath = path.join(root, "src", "console", "client", "index.js");
const scriptPartDir = path.join(root, "src", "console", "client", "parts");
const styleGeneratedPath = path.join(root, "src", "console", "styles", "index.css");
const stylePartDir = path.join(root, "src", "console", "styles", "parts");
const generatedPath = path.join(root, "src", "console", "assets.generated.ts");
const checkOnly = process.argv.includes("--check");

const scriptPartFiles = [
  "state.js",
  "format.js",
  "analysis.js",
  "dashboard.js",
  "detail.js",
  "routing-help.js",
  "authoring.js",
  "runs.js",
  "live.js",
];

const stylePartFiles = [
  "01-tokens.css",
  "02-base-header.css",
  "03-layout-feed.css",
  "04-detail-sheet.css",
  "05-context-help.css",
  "06-gaps.css",
  "07-evidence-details.css",
  "08-authoring-modal.css",
  "09-responsive.css",
];

const [scriptPartSources, stylePartSources] = await Promise.all([
  Promise.all(scriptPartFiles.map((file) => readFile(path.join(scriptPartDir, file), "utf8"))),
  Promise.all(stylePartFiles.map((file) => readFile(path.join(stylePartDir, file), "utf8"))),
]);

const scriptSource = `${scriptPartSources.map((source) => source.trimEnd()).join("\n\n")}\n`;
const scriptSourceFile = [
  "// Generated by scripts/build-console-assets.mjs from src/console/client/parts/.",
  "// Do not edit directly; edit the ordered part files instead.",
  `// Build order: ${scriptPartFiles.join(", ")}`,
  "",
].join("\n");
const styleSource = `${stylePartSources.map((source) => source.trimEnd()).join("\n\n")}\n`;
const styleSourceFile = [
  "/* Generated by scripts/build-console-assets.mjs from src/console/styles/parts/.",
  "   Do not edit directly; edit the ordered part files instead.",
  `   Build order: ${stylePartFiles.join(", ")} */`,
  "",
].join("\n");

const generated = [
  "// Generated by scripts/build-console-assets.mjs from Surface Console asset sources.",
  "// Do not edit directly; edit src/console/client/parts/ or src/console/styles/parts/ instead.",
  "/* eslint-disable */",
  `export const CONSOLE_SCRIPT: string = ${JSON.stringify(scriptSource)};`,
  `export const CONSOLE_CSS: string = ${JSON.stringify(styleSource)};`,
  "",
].join("\n");

if (checkOnly) {
  const [currentScript, currentStyle, currentGenerated] = await Promise.all([
    readFile(scriptSourcePath, "utf8").catch(() => ""),
    readFile(styleGeneratedPath, "utf8").catch(() => ""),
    readFile(generatedPath, "utf8").catch(() => ""),
  ]);
  if (currentScript !== scriptSourceFile || currentStyle !== styleSourceFile || currentGenerated !== generated) {
    throw new Error("Console assets are out of sync. Run npm run build:console-assets.");
  }
  console.log("Surface Console generated assets are synced.");
} else {
  await Promise.all([
    mkdir(path.dirname(scriptSourcePath), { recursive: true }),
    mkdir(path.dirname(styleGeneratedPath), { recursive: true }),
    mkdir(path.dirname(generatedPath), { recursive: true }),
  ]);
  await Promise.all([
    writeFile(scriptSourcePath, scriptSourceFile),
    writeFile(styleGeneratedPath, styleSourceFile),
    writeFile(generatedPath, generated),
  ]);
  console.log("Built Surface Console generated assets.");
}
